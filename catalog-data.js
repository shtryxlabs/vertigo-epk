(function renderVertigoCatalog(){
  const grid = document.querySelector('[data-catalog-grid]');
  const essentialGrid = document.querySelector('[data-essential-grid]');
  const search = document.querySelector('[data-catalog-search]');
  const filters = Array.from(document.querySelectorAll('[data-filter]'));
  const empty = document.querySelector('[data-catalog-empty]');
  const count = document.querySelector('[data-catalog-count]');
  if (!grid) return;

  const essentialTitles = new Set([
    "Vertigo's XXth Anniversary Compilation",
    "Future Frequency",
    "ReVertiGo",
    "ReVertigo 3",
    "The Night Tribe",
    "Dream Selector",
    "Debut",
    "Oddball (Uptempo ReRub)"
  ]);
  let activeFilter = 'all';

  function normalize(value){
    return value.toLowerCase().replace(/[^a-z0-9]+/g, ' ').trim();
  }

  function escapeHtml(value){
    return String(value).replace(/[&<>"']/g, (char) => ({
      '&': '&amp;',
      '<': '&lt;',
      '>': '&gt;',
      '"': '&quot;',
      "'": '&#39;'
    }[char]));
  }

  function isCompilation(release){
    const title = normalize(release.title);
    const artist = normalize(release.artist);
    return artist === 'va' ||
      artist === 'various artists' ||
      title.includes('revertigo') ||
      title.includes('namaste') ||
      title.includes('peace and love') ||
      title.includes('unreal fun') ||
      title.includes('vertigo all around') ||
      title.includes('xxth anniversary') ||
      title.includes('psychedelic oligarchs') ||
      title.includes('culture vulture');
  }

  function isSingleOrEp(release){
    const title = normalize(release.title);
    return release.url.includes('/track/') || title.includes(' ep') || title.startsWith('ep ');
  }

  function isArchiveSeries(release){
    const title = normalize(release.title);
    return title.includes('revertigo') ||
      title.includes('namaste') ||
      title.includes('peace and love') ||
      title.includes('dazzle') ||
      title.includes('vertigo all around') ||
      title.includes('xxth anniversary');
  }

  function matchesFilter(release){
    if (activeFilter === 'all') return true;
    if (activeFilter === 'essential') return essentialTitles.has(release.title);
    if (activeFilter === 'compilation') return isCompilation(release);
    if (activeFilter === 'single') return isSingleOrEp(release);
    if (activeFilter === 'archive') return isArchiveSeries(release);
    if (activeFilter === 'artist') return !isCompilation(release) && !isSingleOrEp(release);
    return true;
  }

  function typeMeta(release){
    if (essentialTitles.has(release.title)) return { label: 'Essential', cls: 'essential' };
    if (isCompilation(release)) return { label: 'VA', cls: 'va' };
    if (isSingleOrEp(release)) return { label: 'EP', cls: 'ep' };
    if (isArchiveSeries(release)) return { label: 'Archive', cls: 'archive' };
    return { label: 'Album', cls: 'album' };
  }

  function cardTemplate(release){
    const index = VERTIGO_CATALOG.indexOf(release);
    const title = escapeHtml(release.title);
    const artist = escapeHtml(release.artist);
    const url = escapeHtml(release.url);
    const cover = escapeHtml(release.cover);
    const t = typeMeta(release);
    return `
      <a class="catalog-card" href="${url}" target="_blank" rel="noopener" data-index="${index}">
        <span class="catalog-cover">
          <img src="${cover}" alt="${title} cover" loading="lazy">
          <span class="catalog-badge ${t.cls}">${t.label}</span>
          <span class="catalog-hover"><span>Quick view</span></span>
        </span>
        <span class="catalog-cap"><strong>${title}</strong><em>${artist}</em></span>
      </a>
    `;
  }

  function currentResults(){
    const query = normalize(search ? search.value : '');
    return VERTIGO_CATALOG.filter((release) => {
      const haystack = normalize(`${release.title} ${release.artist}`);
      return matchesFilter(release) && (!query || haystack.includes(query));
    });
  }

  function render(){
    const results = currentResults();
    grid.innerHTML = results.map(cardTemplate).join('');
    if (empty) empty.style.display = results.length ? 'none' : 'block';
    if (count) count.textContent = `${results.length} shown / ${VERTIGO_CATALOG.length} releases`;
  }

  if (essentialGrid) {
    const essentials = VERTIGO_CATALOG.filter((release) => essentialTitles.has(release.title));
    essentialGrid.innerHTML = essentials.map(cardTemplate).join('');
  }

  filters.forEach((button) => {
    button.addEventListener('click', () => {
      activeFilter = button.dataset.filter;
      filters.forEach((item) => item.classList.toggle('is-active', item === button));
      render();
    });
  });

  if (search) search.addEventListener('input', render);

  // Quick-view modal
  const modal = document.createElement('div');
  modal.className = 'qv-backdrop';
  modal.innerHTML =
    '<div class="qv" role="dialog" aria-modal="true" aria-label="Release details">' +
      '<button class="qv-close" type="button" aria-label="Close">×</button>' +
      '<div class="qv-cover"><img alt=""></div>' +
      '<div class="qv-body">' +
        '<div class="qv-type"></div>' +
        '<div class="qv-title"></div>' +
        '<div class="qv-artist"></div>' +
        '<div class="qv-desc"></div>' +
        '<div class="qv-actions">' +
          '<a class="qv-btn primary" target="_blank" rel="noopener">Listen / Buy on Bandcamp</a>' +
        '</div>' +
      '</div>' +
    '</div>';
  document.body.appendChild(modal);
  const qv = {
    cover: modal.querySelector('.qv-cover img'),
    type: modal.querySelector('.qv-type'),
    title: modal.querySelector('.qv-title'),
    artist: modal.querySelector('.qv-artist'),
    desc: modal.querySelector('.qv-desc'),
    buy: modal.querySelector('.qv-btn'),
    close: modal.querySelector('.qv-close')
  };
  let lastFocus = null;

  function openModal(release){
    const t = typeMeta(release);
    qv.cover.src = release.cover;
    qv.cover.alt = release.title + ' cover';
    qv.type.textContent = t.label;
    qv.title.textContent = release.title;
    qv.artist.textContent = release.artist;
    qv.desc.textContent = release.description || 'Direct Vertigo Records catalog release on Bandcamp.';
    qv.buy.href = release.url;
    lastFocus = document.activeElement;
    modal.classList.add('open');
    document.body.style.overflow = 'hidden';
    qv.close.focus();
  }
  function closeModal(){
    modal.classList.remove('open');
    document.body.style.overflow = '';
    if (lastFocus && lastFocus.focus) lastFocus.focus();
  }
  modal.addEventListener('click', (e) => {
    if (e.target === modal || e.target === qv.close) closeModal();
  });
  document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape' && modal.classList.contains('open')) closeModal();
  });
  function onCardClick(e){
    const card = e.target.closest('.catalog-card');
    if (!card) return;
    e.preventDefault();
    const release = VERTIGO_CATALOG[parseInt(card.dataset.index, 10)];
    if (release) openModal(release);
  }
  grid.addEventListener('click', onCardClick);
  if (essentialGrid) essentialGrid.addEventListener('click', onCardClick);

  // Collapsible full catalog (homepage shows Essential by default)
  const toggle = document.querySelector('[data-catalog-toggle]');
  const full = document.querySelector('[data-catalog-full]');
  const toggleWrap = document.querySelector('[data-catalog-toggle-wrap]');
  if (toggle && full) {
    toggle.addEventListener('click', () => {
      full.hidden = false;
      if (toggleWrap) toggleWrap.style.display = 'none';
      const firstFilter = full.querySelector('.catalog-filter');
      if (firstFilter) firstFilter.focus();
    });
  }

  render();
})();
